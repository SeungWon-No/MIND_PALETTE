                    서비스는 회사가 제7조에 따라서 이용신청을 승낙한 때로부터 즉시 개시됩니다. 다만, 회사의 업무상 또는 기술상의 장애로 인하여 서비스를 즉시 개시하지 못하는 경우 회사는 회원에게 이를 통지합니다.

